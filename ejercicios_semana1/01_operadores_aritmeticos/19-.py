'''
### 19. Promedio  
Pide tres números, calcula el promedio y muestra el resultado.
'''

primerNumero = int(input("Ingrese el primer número: "))
segundoNumero = int(input("Ingrese el segundo número: "))
tercerNumero = int(input("Ingrese el tercer número: "))

print("El promedio de: ",primerNumero,segundoNumero,tercerNumero," es: ",round((primerNumero+segundoNumero+tercerNumero)/3,2))
'''
2. **Contar hacia atrás**:  
   Usa un `while` para imprimir los números del 10 al 1 en orden descendente.
'''
### Se inicializa el contador en 10.
contador = 10

### Bucle a imprimir, restando -1 al contador en cada iteración.
while contador < 11 and contador > 0:
    print(contador)
    contador -= 1